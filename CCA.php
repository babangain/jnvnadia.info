The Vidyalaya aims to provide appropriate opportunities for every single student to fulfill himself/herself on the field. We  recognize the need to coach our best players whilst at the same time providing our less skilful performers with worthwhile and enjoyable activities.<br/>
The Vidyalaya actively encourages the students to mature their talent in the field of Music and Art & provide them with the experience of playing with a large ensemble.<br/>
The World's greatest brotherhood movement Scouts and Guides has brought about great changes in the world. It has strong deep roots here also. The Scouts and Guides are really given opportunities and exposed to overcome the difficulties, when such kind of programme are organize. It is great experience to stand on their own legs and widen their friendships, skills, sharing, caring and mutual help. Adventure activities inculcate leadership qualities etc. Scouting is not only outing. It is a value oriented education programme. We are fully aware that the very Promise and Law gives a concrete role model of an ideal Citizen. The Scouting helps to know the way, show the way and also to lead the way.<br/>
