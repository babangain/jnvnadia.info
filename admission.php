<p style="text-align: center;" align="center"><span style="font-size: medium;"><strong><span>Jawahar Navodaya Vidyalaya Selection Test (JNVST)</span></strong></span></p>
Admission is done through competition test known as JNVST conducted by CBSE and Students are admitted in class VIth. The test consists the objective questions on reasoning, mathematics and language. It is Logically non-verbal and objective in nature and designed so that rural talented students are able to compete without suffering any disadvantage.<br /> <br /> In order to optionally utilize infrastructure facilities, available vacant seats at class IXth level are filled by selection test on the basis of class VIIIth syllabus. Medium of test is Hindi /English as per choice of applicants. Vacant seats of class XIth are filled on the basis of class Xth merit.<br /> <br /> As per scheme maximum of 80 students are admitted in class VIth every year.</span></span></p>
