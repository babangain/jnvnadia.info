<div id="jnv"><center><h2 style="color:red"><b>About JNVs</b></h2></center>
<center><p>Jawahar Navodaya Vidyalayas (JNVs) are a system of alternate schools for gifted students in India. They are run by Navodaya Vidyalaya Samiti, New Delhi, an autonomous organization under the Department of School Education and Literacy, Ministry of Human Resource Development, Government of India. JNVs are fully residential and co-educational schools affiliated to Central Board of Secondary Education (CBSE), New Delhi, with classes from VI to XII standard. JNVs are specifically tasked with finding talented children in rural areas of India and providing them with an education equivalent to the best residential school system, without regard to their family's socio-economic condition.<p>

The Navodaya Vidyalaya System is a unique experiment unparalleled in the annals of school education in India and elsewhere. Its significance lies in the selection of talented rural children as the target group and the attempt to provide them with quality education comparable to the best in a residential school system. Such children are found in all sections of society, and in all areas including the most backward.

JNVs exist all over India, with the exception of Tamil Nadu, where anti Hindi movements were widespread during past times.There are 589 functional schools and 660 sanctioned schools. </p></center>
