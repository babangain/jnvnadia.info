<h2 style="color:red;">Objective</h2>
1. To serve the objectives of excellence coupled with equity and social justice.<br/>
2. To promote national integration by providing opportunities to talented children, largely rural, from different part of the country, to live and learn together and develop their full potential.<br/>
3. To provide good quality modern education, including a strong component of culture, inculcation of values, awareness of the environment, adventure activities and physical education.<br/>
4. To ensure that all students of Navodaya Vidyalaya's attain a reasonable level of competence in three language as envisaged in the Three Language Formula; and<br/>
5. To serve, in each district, as focal point for improvement in quality of school education through sharing of experience and facilities.
<br>
<h2> Salient Features </h2>

<h4> Co-educational Residential Schools with Free Education </h4>

Navodaya Vidyalayas offer free education to talented children from Class-VI to XII. Navodaya Vidyalayas are affiliated to the CBSE. Entry to a Navodaya Vidyalaya is co-educational residential institution providing free boarding and lodging, expenses on uniforms, text books, stationery, to and from rail and bus fare. However, a nominal fee @ Rs. 200/= per month is charged from students of Class IX to XII. The students belonging to SC, ST, categories, girls disabled students and children of the families below poverty line are exempted from this fee.

  <h4>
Adherence to Three Language Formula</h4>
The Regional Language is generally the medium of instruction from Class-VI to VIII and from class-IX onwards, it is English for Science and Mathematics and Hindi for Humanities subjects.
<h4>Promotion of National Integration </h4>
Navodaya Vidyalayas aim at inculcating values of national integration through migration scheme though which the inter regional exchange of students between Hindi and Non-Hindi speaking States and Vice-versa takes place for one academic year. Efforts are made to promote better understanding of the unity in diversity and cultural heritage through various activities.
<h4>
Academic Excellence</h4>
In Jawahar Navodaya Vidyalaya, all out efforts are made to develop competencies among students in scholastic areas of education which leads to effective personally development of each child.


